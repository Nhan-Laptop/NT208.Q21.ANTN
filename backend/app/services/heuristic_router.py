# ── Lazy-loaded Semantic Intent Router (Singleton) ───────────────────────

class SemanticIntentRouter:
    """Encodes user queries with ``all-MiniLM-L6-v2`` and matches them
    against pre-computed intent description embeddings via cosine
    similarity.

    The model is loaded **lazily** on first call to :meth:`classify` so
    it never blocks backend startup.  If ``sentence-transformers`` is not
    installed or the model cannot be loaded, the router degrades
    gracefully and :meth:`classify` returns ``None``.
    """

    _instance: SemanticIntentRouter | None = None
    _lock = threading.Lock()

    def __new__(cls) -> SemanticIntentRouter:
        if cls._instance is None:
            with cls._lock:
                if cls._instance is None:
                    inst = super().__new__(cls)
                    inst._model = None  # type: ignore[attr-defined]
                    inst._intent_embeddings: np.ndarray | None = None  # type: ignore[attr-defined]
                    inst._intent_labels: list[str] = []  # type: ignore[attr-defined]
                    inst._init_attempted: bool = False  # type: ignore[attr-defined]
                    cls._instance = inst
        return cls._instance

    # ------------------------------------------------------------------ #
    # Lazy init
    # ------------------------------------------------------------------ #

    def _ensure_model(self) -> bool:
        """Load the sentence-transformer model and pre-compute intent
        embeddings.  Returns ``True`` if ready."""
        if self._model is not None:
            return True
        if self._init_attempted:
            return False  # already failed once — don't retry

        with self._lock:
            if self._model is not None:
                return True
            self._init_attempted = True
            try:
                from sentence_transformers import SentenceTransformer

                logger.info("SemanticIntentRouter: loading all-MiniLM-L6-v2 …")
                self._model = SentenceTransformer("all-MiniLM-L6-v2")

                # Pre-compute intent embeddings
                labels: list[str] = []
                descriptions: list[str] = []
                for intent, desc in _INTENT_DESCRIPTIONS.items():
                    labels.append(intent)
                    descriptions.append(desc)

                self._intent_labels = labels
                self._intent_embeddings = self._model.encode(
                    descriptions, normalize_embeddings=True,
                )
                logger.info(
                    "SemanticIntentRouter ready — %d intents, embedding dim %d.",
                    len(labels),
                    self._intent_embeddings.shape[1],
                )
                return True
            except Exception as exc:
                logger.warning(
                    "SemanticIntentRouter: ML model unavailable — "
                    "falling back to keyword matching. Error: %s",
                    exc,
                )
                self._model = None
                self._intent_embeddings = None
                return False

    # ------------------------------------------------------------------ #
    # Public API
    # ------------------------------------------------------------------ #

    def classify(self, user_text: str) -> tuple[str | None, float]:
        """Return ``(intent, score)`` or ``(None, 0.0)`` if the model is
        unavailable or the confidence is below threshold."""
        if not self._ensure_model():
            return None, 0.0

        try:
            query_emb = self._model.encode(  # type: ignore[union-attr]
                [user_text], normalize_embeddings=True,
            )
            # Cosine similarity (vectors already L2-normalised → dot product)
            similarities = (query_emb @ self._intent_embeddings.T)[0]  # type: ignore[union-attr]
            best_idx = int(np.argmax(similarities))
            best_score = float(similarities[best_idx])
            best_intent = self._intent_labels[best_idx]

            logger.debug(
                "Semantic intent scores: %s | best=%s (%.3f)",
                {l: f"{s:.3f}" for l, s in zip(self._intent_labels, similarities)},
                best_intent,
                best_score,
            )
            return best_intent, best_score
        except Exception as exc:
            logger.warning("SemanticIntentRouter.classify error: %s", exc)
            return None, 0.0


# Module-level singleton (no work done until classify() is called)
_semantic_router = SemanticIntentRouter()


# ── Keyword-based fallback (the original logic) ─────────────────────────

def _keyword_match(user_text: str) -> str | None:
    """Exact keyword matching against ``_INTENT_KEYWORDS``."""
    low = user_text.lower()
    for intent, keywords in _INTENT_KEYWORDS.items():
        for kw in keywords:
            if kw in low:
                return intent
    return None


def _detect_intent(user_text: str, has_doi: bool) -> str | None:
    """Determine user intent using a three-layer strategy:

    1. **Smart defaults** — DOI presence → RETRACTION; long text without
       DOI → AI_DETECT or GRAMMAR based on surface keywords.
    2. **Semantic routing** — encode user text with ``all-MiniLM-L6-v2``
       and compare against intent description embeddings.
    3. **Keyword fallback** — exact substring matching (safety net).
    """
    low = user_text.lower()

    # Explicit user request should override DOI/default heuristics.
    if _JOURNAL_EXPLICIT_RE.search(low):
        return _Intent.JOURNAL
    if _CITATION_EXPLICIT_RE.search(low) and not _RETRACTION_EXPLICIT_RE.search(low):
        return _Intent.CITATION
    if _RETRACTION_EXPLICIT_RE.search(low) and not _CITATION_EXPLICIT_RE.search(low):
        return _Intent.RETRACTION

    # ── Layer 1: Smart defaults ──────────────────────────────────────
    if has_doi:
        # DOI-only input is an exact identifier lookup. Default to citation
        # verification, unless the user explicitly mentions retraction.
        sem_intent, sem_score = _semantic_router.classify(user_text)
        if sem_intent and sem_score >= 0.55:
            logger.info(
                "DOI smart default overridden by semantic intent=%s (%.3f).",
                sem_intent, sem_score,
            )
            return sem_intent
        if _RETRACTION_EXPLICIT_RE.search(low):
            return _Intent.RETRACTION
        if _CITATION_EXPLICIT_RE.search(low):
            return _Intent.CITATION
        # Bare DOI / DOI URL without explicit keywords → citation
        return _Intent.CITATION

    # Long text without DOI → likely AI detection or grammar checking
    text_len = len(user_text.strip())
    if text_len > 350:
        if _CITATION_EXPLICIT_RE.search(low):
            return _Intent.CITATION
        if _RETRACTION_EXPLICIT_RE.search(low):
            return _Intent.RETRACTION
        # Check for grammar-related surface hints
        _grammar_hints = {"fix", "sửa", "correct", "proofread", "grammar", "lỗi"}
        if any(h in low for h in _grammar_hints):
            return _Intent.GRAMMAR
        return _Intent.AI_DETECT

    # ── Layer 2: Semantic routing ────────────────────────────────────
    sem_intent, sem_score = _semantic_router.classify(user_text)
    if sem_intent is not None and sem_score >= _SEMANTIC_THRESHOLD:
        logger.info(
            "Semantic intent detected: %s (score=%.3f, threshold=%.2f).",
            sem_intent, sem_score, _SEMANTIC_THRESHOLD,
        )
        return sem_intent

    # ── Layer 3: Keyword fallback ────────────────────────────────────
    kw_intent = _keyword_match(user_text)
    if kw_intent is not None:
        logger.info("Keyword fallback matched intent: %s.", kw_intent)
        return kw_intent

    logger.info("No intent detected (semantic best=%.3f, keyword=None).", sem_score)
    return None


# =========================================================================
# Template response generators
